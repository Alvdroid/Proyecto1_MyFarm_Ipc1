
package Farm;




public class Productos {
    
    public static int alimentos;
    public static int materiaPrima;
    public static int contadorAlimento;

    public Productos() {
    }

    public int getContadorAlimento() {
        return contadorAlimento;
    }

    public void setContadorAlimento(int contadorAlimento) {
        this.contadorAlimento = contadorAlimento;
    }
    
    public int getAlimentos() {
        return alimentos;
    }

    public void setAlimentos(int alimentos) {
        this.alimentos = alimentos;
    }

    public int getMateriaPrima() {
        return materiaPrima;
    }

    public void setMateriaPrima(int materiaPrima) {
        this.materiaPrima = materiaPrima;
    }
    
}
